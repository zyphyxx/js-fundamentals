/*
    OPERADORES LÓGICOS:

    && -> AND -> E   -- toda logica tem que ser verdadeira
    || -> OR  -> OU  -- uma das expressões pode ser verdadeira
    !  -> NOT -> NÃO
 */

const and = true && true;
console.log(and);

const or = false || true;
console.log(or);

const not = !true;
console.log(not);